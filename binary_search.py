# Объявляем функцию поиска элемента методом бинарного поиска.
def binary_search(arr, target):
    # Нижняя граница - начало массива
    low = 0
    # Верхняя граница - конец массива
    high = len(arr) - 1

    # Цикл выполняется, пока нижняя граница не станет больше верхней
    while low <= high:
        # Вычисляем индекс среднего элемента
        mid = (low + high) // 2
        # Если средний элемент равен искомому, возвращаем его индекс
        if arr[mid] == target:
            return mid
        # Если средний элемент меньше искомого, смещаем нижнюю границу вправо
        elif arr[mid] < target:
            low = mid + 1
        # Если средний элемент больше искомого, смещаем верхнюю границу влево
        else:
            high = mid - 1

    # Если искомый элемент не найден, возвращаем -1
    return -1